#include<bits/stdc++.h>
using namespace std;
int main(){
    int n,m;
    cin>>n;
    cin>>m;
    cout<<n<<" + "<< m <<" = "<< n+m << "\n";
    cout<<n<<" - "<< m <<" = "<< n-m << "\n";
    cout<<n<<" * "<< m <<" = "<< n*m << "\n";
    cout<<n<<" / "<< m <<" = "<< n/m << "\n";
    cout<<n<<" % "<< m <<" = "<< n%m;
}
